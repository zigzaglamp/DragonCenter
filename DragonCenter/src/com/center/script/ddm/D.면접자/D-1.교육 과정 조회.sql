select
    oc_seq as "번호",
    teacher_name as "교사명",
    course_name as "과정명",
    oc_startdate as "과정 시작일",
    oc_enddate as "과정 종료일",
    subject_name as "과목명",
    os_startdate as "과목 시작일",
    os_enddate as "과목 종료일"
from vwOpenCourseInfo
order by oc_startdate desc;